// entry of program

void main() {
  // declaring a final type variable
  final priceOfPen = 10;

  // printing the value of final variable
  print("Final variable value");
  print("Price of pen: $priceOfPen");

  // final type variable cannot be re-assigned
  // if done, error occurs

  // const variable
  const mobilePhoneName = "Motorola Edge X";

  // printing the value of const variable
  print("\nConst variable value");
  print("Name of mobile: $mobilePhoneName");
}
