// starting the program
void main() {
  // demonstrating the use of arrow function
  // calling the function
  print("My name is ${myName()}");
  print("Value is ${value()}");
  print("Value of PI is ${PI()}");
}

// defining an arrow function
String myName() => "Pratick Baraik";

int value() => 133321;

double PI() => 3.14;
