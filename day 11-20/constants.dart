main() {
  const intConst = 1234;
  const strConst = 'Pratick';
  const doubleConst = 3.14159;

  // printing constants
  print("Integer constant: $intConst");
  print("String constant: $strConst");
  print("Double constant: $doubleConst");

  // printing the type values of variable during runtime
  print("Runtime type of intConst: ${intConst.runtimeType}");
  print("Runtime type of strConst: ${strConst.runtimeType}");
  print("Runtime type of doubleConst: ${doubleConst.runtimeType}");

}
